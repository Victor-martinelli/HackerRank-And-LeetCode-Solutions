public class Chain {
    
    public String name;
    
    public int frequency;

    public Chain(String name, int frequency) {
        this.name = name;
        this.frequency = frequency;
    }

    public String getName() {
        return name;
    }

    public int getFrequency() {
        return frequency;
    }
    
    
    
}
